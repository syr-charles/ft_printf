#include "ft_printf.h"

int		ft_atoi(const char *s)
{
	int		sgn;
	long	val;
	int		i;

	val = 0;
	i = 0;
	if (s[i] == '-')
	{
		sgn = -1;
		i++;
	} else 
		sgn = 1;
	while (s[i] >= '0' && s[i] <= '9')
	{
		val += 10 * val + s[i] - '0';
		i++;
	}
	return (val * sgn);
}
